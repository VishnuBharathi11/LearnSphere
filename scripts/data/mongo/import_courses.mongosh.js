const categoryNames = [...new Set(rawCourses.map(c => String(c.category || "").trim()).filter(Boolean))];

categoryNames.forEach((name) => {
  db.categories.updateOne(
    { name },
    {
      $setOnInsert: {
        description: `${name} category`,
        active: true,
        createdAt: now
      }
    },
    { upsert: true }
  );
});

const categoryMap = {};
db.categories.find({}, { name: 1 }).forEach((cat) => {
  categoryMap[cat.name] = String(cat._id);
});

let inserted = 0;
let updated = 0;

rawCourses.forEach((course) => {
  const title = String(course.courseName || "").trim();
  if (!title) return;

  const categoryId = categoryMap[String(course.category || "").trim()] || null;
  const instructorId = instructorMap[String(course.instructor || "").trim()] || "demo_instructor_generic";

  const payload = {
    title,
    description: `${title} by ${course.instructor}. Level: ${course.level}. Includes ${course.lessons} lessons.`,
    thumbnail: "",
    price: Number(course.price || 0),
    categoryId,
    instructorId,
    status: "PUBLISHED",
    createdAt: now
  };

  const existing = db.courses.findOne({ title });
  if (existing) {
    db.courses.updateOne(
      { _id: existing._id },
      { $set: { ...payload, createdAt: existing.createdAt || now } }
    );
    updated += 1;
  } else {
    db.courses.insertOne(payload);
    inserted += 1;
  }
});

printjson({
  totalInput: rawCourses.length,
  inserted,
  updated,
  totalCoursesInCollection: db.courses.countDocuments({}),
  totalCategoriesInCollection: db.categories.countDocuments({})
});
